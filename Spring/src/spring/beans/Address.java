package spring.beans;

public class Address {
	
	private String area;
	
	private String city;
	
	private String state;
	
	private int pin;
	
	public void setArea(String area) {
		this.area = area;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public void setState(String state) {
		this.state = state;
	}

	public void setPin(int pin) {
		this.pin = pin;
	}

	
}
